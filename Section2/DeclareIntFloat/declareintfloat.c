/*Program that declares ints and floats, doesn't initialise them, and prints their output.*/

#include <stdio.h>

int main(void)
{
    int integer1, integer2, integer3;
    float float1, float2, float3;

    printf("Selected number1: %d\n", integer1);
    printf("Selected number2: %d\n", integer2);
    printf("Selected number3: %d\n", integer3);
    
    printf("Selected float1: %f\n", float1);
    printf("Selected float2: %f\n", float2);
    printf("Selected float3: %f\n", float3);

    return 0;
}
